# You can access tuple items by referring to the index number, inside square brackets:
mytuple = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
print(mytuple[0])
print(mytuple[-1])
print(mytuple[2:5])
print(mytuple[:3])
print(mytuple[3:])
print(mytuple[-4:-1])

if "melon" in mytuple:
    print("Yes,'melon' is  in the mytuple ")
