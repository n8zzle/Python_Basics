# Data Types
x = "Hello World"
print(type(x))
x = 20
print(type(x))
x = 20.5
print(type(x))
x = 1j
print(type(x))
x = ["apple", "banana", "cherry"]
print(type(x))
x = ("MacOS", "Linux", "Windows")
print(type(x))
x = range(6)
print(type(x))
x = {"name": "Igors", "age": 26}
print(type(x))
x = {"apple", "banana", "cherry"}
print(type(x))
x = frozenset({"user", "admin", "superadmin"})
print(type(x))
x = True
print(type(x))
x = b"Hello"
print(type(x))
x = bytearray(5)
print(type(x))
x = memoryview(bytes(5))
print(type(x))
x = None
print(type(x))
