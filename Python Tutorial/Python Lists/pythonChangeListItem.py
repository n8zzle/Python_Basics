# Changing list item
mylist = ["apple", "orange", "kiwi"]
mylist[1] = "cherry"
print(mylist)

# Change a Range of Item Values and insert new
mylist[0:3] = ["Igors", "Cickalenko", "Berlin"]
print(mylist)

# Insert "watermelon" as the third item:
thislist = ["apple", "banana", "cherry"]
thislist.insert(2, "watermelon")
print(thislist)
