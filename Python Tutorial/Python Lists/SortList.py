# List objects have a sort() method that will sort the list alphanumerically, ascending, by default:
thislist = ["orange", "mango", "kiwi", "pineapple", "banana"]
thislist.sort()
print(thislist)

thislist = [100, 75, 43, 65, 10, 20, 80]
thislist.sort()
print(thislist)

thislist = [100, 75, 43, 65, 10, 20, 80]
thislist.sort(reverse=True)
print(thislist)
